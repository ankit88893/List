public class Student {
	
	private int age;
	private String rollno;
	
	Student(int age, String rollno){
		

	this.age=age;
	this.rollno= rollno;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getRollno() {
		return rollno;
	}

	public void setRollno(String rollno) {
		this.rollno = rollno;
	}

}
